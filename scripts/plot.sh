DIR_SCRIPT="$( cd "$( dirname "${BASH_SOURCE[0]}"  )" >/dev/null 2>&1 && pwd  )"

source $DIR_SCRIPT/const.sh

function plot() {
	exp=$1
	version=$2

	echo "plotting $exp $version"
	Rscript --vanilla $DIR_RSCRIPTS/$exp.R results/cooked/$version/$exp.csv
}

hash=08af852

# Experiments
for exp in fork-join;
do
	plot $exp $hash
done

rm Rplots.pdf
mv *.pdf $DIR_PLOTS
